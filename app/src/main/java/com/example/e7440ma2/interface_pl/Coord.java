public class Coord {

    private float x;
    private float y;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public Coord(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public Coord() {
        super();
    }
}
